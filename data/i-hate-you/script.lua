function onBeatHit()

    if curBeat >= 48 and curBeat <= 96 then
        triggerEvent('Add Camera Zoom','0.008','')
    end

    if curBeat >= 100 and curBeat <= 226 then
        triggerEvent('Add Camera Zoom','0.008','')
    end

    if curBeat >= 228 and curBeat <= 356 then
        triggerEvent('Add Camera Zoom','0.008','')
    end
end